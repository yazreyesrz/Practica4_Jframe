        // TODO add your handling code here:
       String Nss = String.valueOf(NSS);
       String ventas = String.valueOf(Ventas);
       
       EmpleadoBaseMasComision EmpleaditoConSueldo = new EmpleadoBaseMasComision(Nombre,Apellido,NSS,Ventas,tarifa,Sueldo);
