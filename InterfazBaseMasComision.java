    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        //Guardar el contenido de los cuadros de texto
        Nombre = txtNombre.getText();
        Apellido = txtApellido.getText();
        NSS = txtNSS.getText();
        Ventas = Double.parseDouble(txtVentas.getText());
        tarifa = 0.05;
        Sueldo = Double.parseDouble(txtSueldoBase.getText());
        
        //Pase de parametros a un objeto
        EmpleadoBaseMasComision EmpleaditoConSueldo = new EmpleadoBaseMasComision();
        EmpleaditoConSueldo.setNombre(Nombre);
        EmpleaditoConSueldo.setApellido(Apellido);
        EmpleaditoConSueldo.setNumeroSeguroSocial(NSS);
        EmpleaditoConSueldo.setVentasBrutas(Ventas);
        EmpleaditoConSueldo.setTarifaComision(tarifa);
        EmpleaditoConSueldo.setSalarioBase(Sueldo);
        
        //Borrar el contenido de los cuadros de texto
        txtNombre.setText(null);
        txtApellido.setText(null);
        txtNSS.setText(null);
        txtVentas.setText(null);
        txtSueldoBase.setText(null);
        
    }//GEN-LAST:event_btnAgregarActionPerformed

